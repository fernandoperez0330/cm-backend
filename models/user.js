/**
 * Method to make a quick register (only apply for end-user)
 * @param  Context ctx
 * @param  String accountNumber the unique account number (username)
 * @return Promise          The token generated to validate the code sent
 */
User.prototype.quickRegister = async function(ctx,accountNumber){
  var orgIp = Common.ip.address();

  //verify if the user in the app already exists
  var user = await User.findOne({where: {username: accountNumber}});
  var saved = false;
  if (user === null){
    //proceed to register the new user
    var password = generator.generate({length: 8,numbers: true});
    ////global.logger.debug("Password generated by '" + accountNumber + "':",password);
    password =  bcrypt.hashSync(password, Config.crypt.salt.rounds);

    user = User.build({
      username: accountNumber,
      password: password,
      placcountNumber: accountNumber,
      statusId: UserStatus.TYPES.PENDING,
      timezoneId: Timezone.ID.AMERICA__NEW_YORK,
      userGroupId: UserGroup.TYPES.USER
    });
    saved = await user.save().catch(err=>{
      console.log("err",err);
    });

    if (!saved){
      global.logger.default.error("the user " + accountNumber + " cannot be saved");
      resolve(null);
      return;
    }
  }
  return new Promise(async function(resolve,reject){
    var db = new MsDatabase();
    db.execute(async function(pool,result){
      var resultRegister = await pool.request()
        .input('iAcct_GUID',MsDatabase.Mssql.VarChar,Config.db.pinless.acct_guid)
        .input('iAcct_User',MsDatabase.Mssql.VarChar,Config.db.pinless.acct_user)
        .input('iAcct_Pass',MsDatabase.Mssql.VarChar,Config.db.pinless.acct_pass)
        .input('iPL_User',MsDatabase.Mssql.VarChar,accountNumber)
        .input('iOrg_IP',MsDatabase.Mssql.VarChar,orgIp)
        .output('oSession_ID', MsDatabase.Mssql.VarChar)
        .output('oDevice_GUID', MsDatabase.Mssql.VarChar)
        .execute("spSoftPhone_Simple_Register");
        if (!(typeof resultRegister === "object" && typeof resultRegister['returnValue'] === "number" && resultRegister['returnValue'] === Config.db.pinless.return_value.success)){
          var wserror = db.mapError(resultRegister['returnValue'],"SIMPLE_REGISTER");
          reject(wserror);
          return;
        }

        //recordsets=[], recordset=undefined, oSession_ID=1F8BCF55-9664-4F64-B2D8-173FD42E0E71, oDevice_GUID=1826AE07-28A3-494F-9E95-638747B86F90, rowsAffected=[], returnValue=200
        //@iAcct_GUID, @iAcct_User, @iAcct_Pass, @iPL_User, @iOrg_IP, @oSession_ID, @oDevice_GUID
        //console.log(resultRegister);
        var outputResult = typeof resultRegister['output'] === "object" ? resultRegister['output'] : {};
        var output = {user: user};
        output.token = User.encodeToken(outputResult['oSession_ID'],outputResult['oDevice_GUID'],user.userId);
        if (output.token === null){
          global.logger.default.error("error generating token",{
            output: output
          });
          reject(5002);
          return;
        }
        resolve(output);
    }).catch(err => {
        global.logger.default.error("Error registering the user",{
          error: err
        });
        reject(db.mapError("500"));
    });
  });
}


/**
 * Method to confirm the user (by SMS so far)
 * @param  {[type]} ctx              Context
 * @param  {[type]} token            token
 * @param  {[type]} verificationCode Verification Code
 * @return Promise                   [description]
 */
User.prototype.callCode = async function(ctx, token){
  var orgIp = Common.ip.address();

  var errorRegisteringUserCode = "5002";

  return new Promise(async function(resolve,reject){
    var decodedToken = User.decodeToken(token);
    if (decodedToken === null){
      global.logger.default.error("invalid token", {
        token: token
      });
      reject("4006");
      return;
    }

    var db = new MsDatabase();
    db.execute(async function(pool,result){
      var resultGetCode = await pool.request()
      .input('iSession_Id',MsDatabase.Mssql.VarChar,decodedToken.session_id)
      .input('iAcct_GUID',MsDatabase.Mssql.VarChar,Config.db.pinless.acct_guid)
      .input('iDevice_Id',MsDatabase.Mssql.VarChar,decodedToken.device_id)
      .input('iOrg_IP',MsDatabase.Mssql.VarChar,orgIp)
      .output('oAccount_Id',MsDatabase.Mssql.VarChar)
      .output('oLocale',MsDatabase.Mssql.VarChar)
      .output('oCode',MsDatabase.Mssql.VarChar)
      .execute("spSoftPhone_Get_Code");

      if (!(typeof resultGetCode === "object" && typeof resultGetCode['returnValue'] === "number" && resultGetCode['returnValue'] === Config.db.pinless.return_value.success)){
        var wserror = db.mapError(resultGetCode['returnValue'],"RETRIEVE_CODE");
        reject(wserror);
        return;
      }

      var outputResult = typeof resultGetCode['output'] === "object" ? resultGetCode['output'] : {};

      //proceed to request the call
      var ivrApiService = new IVRApiService();
      var resultCode = await ivrApiService.sendCode(outputResult['oAccount_Id'],outputResult['oCode'],outputResult['oLocale']);
      if (!resultCode){
        reject("5006");
        return;
      }
      //end: proceed to request the call
      resolve(resultCode);
    }).catch(err => {
        global.logger.default.error("Error retrieving the sms code",{
          error: err
        });
        reject(db.mapError("500"));
    });
  });
}


