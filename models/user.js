/**
 * Method to confirm the user (by SMS so far)
 * @param  {[type]} ctx              Context
 * @param  {[type]} token            token
 * @param  {[type]} verificationCode Verification Code
 * @return Promise                   [description]
 */
User.prototype.confirmUser = async function(ctx, token, verificationCode){
  var orgIp = Common.ip.address();
  var resend = verificationCode === "RESEND";
  var errorRegisteringUserCode = "5002";

  return new Promise(async function(resolve,reject){
    var decodedToken = User.decodeToken(token);
    if (decodedToken === null){
      global.logger.default.error("invalid token",{
        token: token
      });
      reject("4006");
      return;
    }
    //console.log("decodedToken",decodedToken);
    //verify if the user in the app already exists
    var user = await User.findById(decodedToken.user_id);

    if (user === null){
      global.logger.default.error("the user is null",{
        decodeToken: decodedToken,
        user: user
      });
      reject("4006");
      return;
    }

    var db = new MsDatabase();
    db.execute(async function(pool,result){
      var resultConfirm = await pool.request()
      .input('iSession_Id',MsDatabase.Mssql.VarChar,decodedToken.session_id)
      .input('iAcct_GUID',MsDatabase.Mssql.VarChar,Config.db.pinless.acct_guid)
      .input('iDevice_Id',MsDatabase.Mssql.VarChar,decodedToken.device_id)
      .input('iVrf_Code',MsDatabase.Mssql.VarChar,verificationCode)
      .input('iOrg_IP',MsDatabase.Mssql.VarChar,orgIp)
      .execute("spSoftPhone_Vrf_Code_crAccount");

      if (!(typeof resultConfirm === "object" && typeof resultConfirm['returnValue'] === "number" && resultConfirm['returnValue'] === Config.db.pinless.return_value.success)){
        var wserror = db.mapError(resultConfirm['returnValue'],"VERIFICATION_CODE");
        reject(wserror);
        return;
      }

      if (resend){
        resolve(user);
        return;
      }

      //proceed to update the device id and status
      user.deviceId = decodedToken.device_id;
      user.statusId = UserStatus.TYPES.ACTIVE;
      var saved = await user.save();

      if (!saved){
        global.logger.default.error("error updating the user",{
          user: user
        });
        //reject with error registering the user
        reject("5002");
        return;
      }
      //end: proceed to update the device id and status
        resolve(user);
    }).catch(err => {
        global.logger.default.error("Error confirming user",{
          error: err
        });
        reject(db.mapError("500"));
    });
  });
};

/**
 * Method to decode the token generated in quick register
 * @param  {[type]} token [description]
 * @return {[type]}       [description]
 */
User.decodeToken = function(token){
  var token = new Buffer(token, 'base64').toString('ascii');
  token = token.split("|");
  if (token.length < 3) return null;

  var outputToken = {};
  outputToken.session_id = token[0];
  outputToken.device_id  = token[1];
  outputToken.user_id    = token[2];

  for(var k in outputToken) if (outputToken[k] === null) return null;

  return outputToken;
};

/**
 * Method to encode the token generated in quick register
 * @param  {String} session_id  [description]
 * @param  {String} device_id   [description]
 * @param  {Number} user_id     [description]
 * @return {[type]}             [description]
 */
User.encodeToken = function(session_id,device_id,user_id){
  if (typeof session_id !== "string" || typeof device_id !== "string") return null;
  var token = session_id + "|" + device_id + "|" + user_id;
  token = new Buffer(token).toString('base64');
  return token;
};

